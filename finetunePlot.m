function finetunePlot( plotHandle )

%make plot curent frontmost plot
set(0, 'CurrentFigure', plotHandle);
set(0,'ShowhiddenHandles','on');

%adjust axis
set( gca                       , ...
    'FontName'   , 'AvantGarde' );
%set([hTitle, hXLabel, hYLabel, hText], ...
%    'FontName'   , 'AvantGarde');
%set([hLegend, gca]             , ...
%   'FontSize'   , 8           );
%set([hXLabel, hYLabel, hText]  , ...
%    'FontSize'   , 10          );
%set( hTitle                    , ...
%    'FontSize'   , 12          , ...
%    'FontWeight' , 'bold'      );

set(gca, ...
    'Box'         , 'off'     , ...
    'TickDir'     , 'out'     , ...
    'TickLength'  , [.02 .02] , ...
    'XMinorTick'  , 'on'      , ...
    'YMinorTick'  , 'on'      , ...
    ...%'YGrid'       , 'on'      , ...
    'XColor'      , [.3 .3 .3], ...
    'YColor'      , [.3 .3 .3], ...
    ...%'YTick'       , 0:500:2500, ...
    'LineWidth'   , 1         );



%adjust lines
lineobj = findobj( plotHandle, 'type', 'line' );
set( lineobj, 'LineWidth', 2 );

%adjust title
titleobj = findobj( plotHandle, 'type', 'title' );
set( titleobj, 'FontName', 'AvantGarde' );

%adjust legend
legendobj = findobj( plotHandle, 'Type', 'axes', 'Tag', 'legend' );
set( legendobj, 'FontName', 'AvantGarde' );
end